    Y4 DOUBLE,
    X5 DOUBLE,
    Y5 DOUBLE,
    X6 DOUBLE,
    Y6 DOUBLE,
	X7 DOUBLE,
    Y7 DOUBLE,
    X8 DOUBLE,
    Y8 DOUBLE,
    X9 DOUBLE,
    Y9 DOUBLE,
    X10 DOUBLE,
    Y10 DOUBLE,
	X11 DOUBLE,
    Y11 DOUBLE,
    X12 DOUBLE,
    Y12 DOUBLE,
    X13 DOUBLE,
    Y13 DOUBLE,
    X14 DOUBLE,
    Y14 DOUBLE,
	X15 DOUBLE,
    Y15 DOUBLE,
    X16 DOUBLE,
    Y16 DOUBLE,
    X17 DOUBLE,
    Y17 DOUBLE,
    INDEX idx_pic_loc (PIC_LOC)
